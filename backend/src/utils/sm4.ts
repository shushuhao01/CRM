const FK = new Uint32Array([0xa3b1bac6, 0x56aa3350, 0x677d9197, 0xb27022dc]);

// CK[i] 各字节 = (4i + j) * 7 mod 256
const CK = new Uint32Array(32);
for (let i = 0; i < 32; i++) {
  CK[i] = (
    (((4 * i) * 7 % 256) << 24) |
    (((4 * i + 1) * 7 % 256) << 16) |
    (((4 * i + 2) * 7 % 256) << 8) |
    ((4 * i + 3) * 7 % 256)
  ) >>> 0;
}

function rotl(x: number, n: number): number {
  return ((x << n) | (x >>> (32 - n))) >>> 0;
}

/** 非线性变换 τ：按字节过 S 盒 */
function tau(a: number): number {
  return (
    (SBOX[(a >>> 24) & 0xff] << 24) |
    (SBOX[(a >>> 16) & 0xff] << 16) |
    (SBOX[(a >>> 8) & 0xff] << 8) |
    SBOX[a & 0xff]
  ) >>> 0;
}

/** 加密线性变换 L */
function lTransform(b: number): number {
  return (b ^ rotl(b, 2) ^ rotl(b, 10) ^ rotl(b, 18) ^ rotl(b, 24)) >>> 0;
}

/** 密钥扩展线性变换 L' */
function lPrimeTransform(b: number): number {
  return (b ^ rotl(b, 13) ^ rotl(b, 23)) >>> 0;
}

/** 密钥扩展：生成 32 轮轮密钥 */
function expandKey(key: Buffer): Uint32Array {
  const mk = new Uint32Array(4);
  for (let i = 0; i < 4; i++) {
    mk[i] = key.readUInt32BE(i * 4);
  }
  const k = new Uint32Array(36);
  for (let i = 0; i < 4; i++) {
    k[i] = (mk[i] ^ FK[i]) >>> 0;
  }
  const rk = new Uint32Array(32);
  for (let i = 0; i < 32; i++) {
    k[i + 4] = (k[i] ^ lPrimeTransform(tau((k[i + 1] ^ k[i + 2] ^ k[i + 3] ^ CK[i]) >>> 0))) >>> 0;
    rk[i] = k[i + 4];
  }
  return rk;
}

/** 加密单个 16 字节分组 */
function encryptBlock(block: Buffer, rk: Uint32Array): Buffer {
  const x = new Uint32Array(36);
  for (let i = 0; i < 4; i++) {
    x[i] = block.readUInt32BE(i * 4);
  }
  for (let i = 0; i < 32; i++) {
    x[i + 4] = (x[i] ^ lTransform(tau((x[i + 1] ^ x[i + 2] ^ x[i + 3] ^ rk[i]) >>> 0))) >>> 0;
  }
  const out = Buffer.alloc(16);
  out.writeUInt32BE(x[35], 0);
  out.writeUInt32BE(x[34], 4);
  out.writeUInt32BE(x[33], 8);
  out.writeUInt32BE(x[32], 12);
  return out;
}

/**
 * 解析 SM4 密钥：支持 Base64 编码的 16 字节密钥或 16 字符明文密钥
 */
export function parseSm4Key(key: string): Buffer {
  const trimmed = (key || '').trim();
  try {
    const decoded = Buffer.from(trimmed, 'base64');
    // Base64 反解校验：能还原且恰好16字节才认为是Base64密钥
    if (decoded.length === 16 && decoded.toString('base64').replace(/=+$/, '') === trimmed.replace(/=+$/, '')) {
      return decoded;
    }
  } catch { /* 非Base64，走明文 */ }
  const utf8 = Buffer.from(trimmed, 'utf8');
  if (utf8.length === 16) {
    return utf8;
  }
  throw new Error('SM4密钥必须是16字节（支持Base64编码或16字符明文）');
}

/**
 * SM4-ECB 加密原始分组数据（不填充，长度必须为16的倍数）
 * 主要用于算法自检
 */
export function sm4EncryptRaw(data: Buffer, key: Buffer): Buffer {
  if (key.length !== 16) {
    throw new Error('SM4密钥长度必须为16字节');
  }
  if (data.length % 16 !== 0) {
    throw new Error('SM4原始加密数据长度必须为16的倍数');
  }
  const rk = expandKey(key);
  const cipher = Buffer.alloc(data.length);
  for (let offset = 0; offset < data.length; offset += 16) {
    encryptBlock(data.subarray(offset, offset + 16), rk).copy(cipher, offset);
  }
  return cipher;
}

/**
 * SM4-ECB 加密（PKCS7 填充），返回 Base64 密文
 * @param plaintext UTF-8 明文
 * @param key 16 字节密钥
 */
export function sm4EncryptEcbBase64(plaintext: string, key: Buffer): string {
  const data = Buffer.from(plaintext, 'utf8');
  const padLen = 16 - (data.length % 16);
  const padded = Buffer.concat([data, Buffer.alloc(padLen, padLen)]);
  return sm4EncryptRaw(padded, key).toString('base64');
}
